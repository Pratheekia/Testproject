package Patternprog;

import java.util.Scanner;

public class pat1while {

	public static void main(String[] args) {
		Scanner scan=new Scanner(System.in);
        System.out.println("Enter the num" );
        int n =scan.nextInt();
        int i=1;
        while(i<=n)
        {
        	System.out.print("*");
        	  i++;
        }
      

	}

}
