import java.util.Scanner;
public class Implicit3 {

	public static void main(String[] args) {
		Scanner scan = new Scanner (System.in);
		System.out.println("enter then byte value");
		byte a =scan.nextByte();
		System.out.println("enter then short value");
		short b =scan.nextShort();
		System.out.println("enter then int value");
		int c =scan.nextInt();
		System.out.println("enter then long value");
		long d =scan.nextLong();
		System.out.println("enter then float value");
		float e =scan.nextFloat();
		System.out.println("enter then double value");
		double f =scan.nextDouble();
		System.out.println("enter then boolean value");
		boolean g =scan.nextBoolean();
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		

	}

}
